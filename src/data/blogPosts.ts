// Helper function to get a translated blog post
export function getTranslatedBlogPost(post: BlogPost, language: string = 'en'): BlogPost {
  if (language === 'en' || !post.translations || !post.translations[language]) {
    return post;
  }

  const translation = post.translations[language];
  
  return {
    ...post,
    title: translation.title,
    excerpt: translation.excerpt,
    category: translation.category
  };
}

// Helper function to get all blog posts in a specific language
export function getTranslatedBlogPosts(language: string = 'en'): BlogPost[] {
  return blogPosts.map(post => getTranslatedBlogPost(post, language));
}
